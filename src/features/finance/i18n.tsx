export type FinanceTranslationKey = keyof typeof financeTranslations.en;

type FinanceI18nContextValue = {
  language: FinanceLanguage;
  setLanguage: (language: FinanceLanguage) => void;
  t: (key: FinanceTranslationKey, values?: Record<string, string | number>) => string;
  categoryName: (name?: string | null) => string;
};

const FinanceI18nContext = createContext<FinanceI18nContextValue | null>(null);

export function FinanceI18nProvider({ children }: { children: React.ReactNode }) {
  const [language, setLanguageState] = useState<FinanceLanguage>('en');

  useEffect(() => {
    const saved = localStorage.getItem(FINANCE_LANGUAGE_STORAGE_KEY);
    if (saved === 'en' || saved === 'es') {
      setLanguageState(saved);
    }
  }, []);

  const setLanguage = useCallback((nextLanguage: FinanceLanguage) => {
    setLanguageState(nextLanguage);
    localStorage.setItem(FINANCE_LANGUAGE_STORAGE_KEY, nextLanguage);
  }, []);

  const t = useCallback((key: FinanceTranslationKey, values?: Record<string, string | number>) => {
    let template: string = financeTranslations[language][key] || financeTranslations.en[key] || key;
    if (!values) return template;

    for (const [name, value] of Object.entries(values)) {
      template = template.replaceAll(`{${name}}`, String(value));
    }

    return template;
  }, [language]);

  const categoryName = useCallback((name?: string | null) => {
    if (!name) return t('uncategorized');
    return defaultCategoryLabels[language][name] || name;
  }, [language, t]);

  const value = useMemo(
    () => ({ language, setLanguage, t, categoryName }),
    [categoryName, language, setLanguage, t]
  );

  return (
    <FinanceI18nContext.Provider value={value}>
      {children}
    </FinanceI18nContext.Provider>
  );
}

export function useFinanceI18n() {
  const context = useContext(FinanceI18nContext);
  if (!context) {
    throw new Error('useFinanceI18n must be used within FinanceI18nProvider');
  }
  return context;
}
